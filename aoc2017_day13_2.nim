import strutils
import tables

proc solve(s: string): int =
  var
    firewall = newTable[int, int]()
    maxDepth = 0
  for line in s.split("\n"):
    let
      parts = line.split(": ")
      depth = parts[0].parseInt
    firewall[depth] = parts[1].parseInt
    maxDepth = depth
  var
    delay = 0
    isCaught = false
  # simulate a delay until we don't get caught
  while true:
    for dd in delay .. delay + maxDepth:
      let depth = dd - delay
      if depth notin firewall:
        continue
      let
        r = firewall[depth]
        # Why? see previous problem
        pos = dd mod ((r - 1) * 2)
      if pos == 0:
        isCaught = true
        break
    if not isCaught:
      break
    isCaught = false
    inc delay
  result = delay

